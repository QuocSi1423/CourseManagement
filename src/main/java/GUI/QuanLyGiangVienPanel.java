    private void btSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSuaActionPerformed
        // TODO add your handling code here:

        int lecturerID= (int) tbDanhSach.getValueAt(tbDanhSach.getSelectedRow(), 0);
        LecturerDTO lecDTO= lecBus.getALecturerByID(lecturerID);

        if(lecDTO!=null){
            String lecSurName= tfHo.getText();
            String lecName= tfTen.getText();
            Date selectedDate = (Date) dcNgayThue.getDate();
            LocalDateTime hireDate =  LocalDateTime.of(selectedDate.getYear()+1900, selectedDate.getMonth(),
                selectedDate.getDate() , selectedDate.getHours(), selectedDate.getMinutes());

            lecDTO.setFirstName(lecSurName);
            lecDTO.setLastName(lecName);
            lecDTO.sethireDate(hireDate);
            int result = lecBus.updateALecturer(lecDTO);

            if (result == 1) {
                JOptionPane.showMessageDialog(this, "Cập nhật thành công!");
                setTable();
            } else {
                JOptionPane.showMessageDialog(this, "Có lỗi xảy ra trong quá trình cập nhật!");
            }
        }
        else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một giảng viên để sửa đổi!");
        }
    }//GEN-LAST:event_btSuaActionPerformed

