    private void setTable() {
    List<LecturerDTO> listLecturer = lecBus.getAllLecturers();

    DefaultTableModel model = (DefaultTableModel) tbDanhSach.getModel();
    model.setRowCount(0); // Reset bảng bằng cách xóa tất cả các hàng

    ImageIcon trashIcon = new ImageIcon("C:\\Users\\DELL\\Desktop\\DoAnJava\\CourseManagement-Embedded\\src\\main\\java\\ICON\\delete.png");

    // Sử dụng renderer mặc định để hiển thị nút với hình ảnh
    DefaultTableCellRenderer buttonRenderer = new DefaultTableCellRenderer();
    buttonRenderer.setIcon(trashIcon);

    // Đặt renderer cho cột chứa nút
    tbDanhSach.getColumnModel().getColumn(5).setCellRenderer(buttonRenderer);

    for (LecturerDTO lecturer : listLecturer) {
        Object[] rowData = {lecturer.getID(), lecturer.getFirstName(), lecturer.getLastName(), null, lecturer.gethireDate(), " "};
        model.addRow(rowData);
    }

    tbDanhSach.addMouseListener(new MouseAdapter() {
        @Override
        public void mouseClicked(MouseEvent e) {
            int row = tbDanhSach.rowAtPoint(e.getPoint());
            int column = tbDanhSach.columnAtPoint(e.getPoint());
            
            
            tbDanhSach.setRowSelectionInterval(row, row);
            
           
            if (column == 5) {
               int confirm = JOptionPane.showConfirmDialog(QuanLyGiangVienPanel.this, "Bạn có muốn xóa hàng này không?", "Xác nhận xóa", JOptionPane.OK_CANCEL_OPTION);
                if (confirm == JOptionPane.OK_OPTION) {
                lecBus.removeALecturer((int) tbDanhSach.getValueAt(tbDanhSach.getSelectedRow(),0));
                model.removeRow(row);
                }
            }
        }
    });
}
    private void btThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemActionPerformed
        // TODO add your handling code here:

        int lecturerID = lecBus.getAllLecturers().size()+1 ;
        String surName= tfHo.getText().toString();
        String name= tfTen.getText().toString();

        Date selectedDate = (Date) dcNgayThue.getDate();
        LocalDateTime hireDate =  LocalDateTime.of(selectedDate.getYear()+1900, selectedDate.getMonth(),
            selectedDate.getDate() , selectedDate.getHours(), selectedDate.getMinutes());

        if(surName.equals("")|| name.equals("")  ){
            JOptionPane.showMessageDialog(this, "Vui lòng điền đầy đủ thông tin!");
        }
        else{
            LecturerDTO lecturer= new LecturerDTO(lecturerID,surName,name, hireDate);

            int result=lecBus.createALecturer(lecturer);
            JOptionPane.showMessageDialog(this, "Test");
            if( result==1){
                JOptionPane.showMessageDialog(this, "Thêm thành công!");}
            else{
                JOptionPane.showMessageDialog(this, "Thêm thất bại!");
            }
        }

        setTable();
    }//GEN-LAST:event_btThemActionPerformed

