        String tenKhoaHoc= tfTenKhoaHoc.getText();
        int soTinChi= Integer.parseInt(tfSoTinChi.getText());
        String url= tfURL.getText();
//        String department= (String )cbKhoa.getSelectedItem();
        
        OnlineCourseDTO onCouDTO= onCouBus.getAnOnlineCourse(CourseID);
        CourseDTO couDTO= couBus.getACourseByID(CourseID);
        
        
        onCouDTO.setUrl(url);
        couDTO.setCredits(soTinChi);
        couDTO.setTitle(tenKhoaHoc);
        
        couDTO.setDepartment(depBus.getDepartmentByID(DepartmentID.get(selectedIndex)));
        
        
        int result1= couBus.updateCourse(couDTO);
        int result2= onCouBus.updateAnOnlineCourse(onCouDTO);
        if( result1==1 && result2==1){
            JOptionPane.showMessageDialog(this, "Lưu thành công");
        }
        else{
            JOptionPane.showMessageDialog(this, "Lưu thất bại");
        }
        
