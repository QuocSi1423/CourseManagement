    private void setData() {
    List<LecturerDTO> listLecturer = couBus.getLecturersOfCourse(CourseID);

    DefaultTableModel model = (DefaultTableModel) tbDanhSach.getModel();
    model.setRowCount(0); // Reset bảng bằng cách xóa tất cả các hàng
    for (LecturerDTO lecturer : listLecturer) {
        Object[] rowData = {lecturer.getID(), lecturer.getFirstName(), lecturer.getLastName(), lecturer.getOfficeAssignment().getLocation()};
        model.addRow(rowData);
    }

    tbDanhSach.addMouseListener(new MouseAdapter() {
        @Override
        public void mouseClicked(MouseEvent e) {
            int row = tbDanhSach.rowAtPoint(e.getPoint());
            int column = tbDanhSach.columnAtPoint(e.getPoint());
            
            tbDanhSach.setRowSelectionInterval(row, row);
        }
    });
    
    OnlineCourseDTO onCouDTO= onCouBus.getAnOnlineCourse(CourseID);
    System.out.println(CourseID+"");
    CourseDTO couDTO= couBus.getACourseByID(CourseID);
    tfMaKhoaHoc.setText(onCouDTO.getCourseID()+"");
    tfTenKhoaHoc.setText(couDTO.getTitle());
    tfSoTinChi.setText(couDTO.getCredits()+"");
    
    tfURL.setText(onCouDTO.getUrl()+"");
    
//    ------------->set Combobox KHoa <---------------------
        List<DepartmentDTO> listDepartment = depBus.getAllDepartment();
        for(DepartmentDTO a : listDepartment){
            this.DepartmentID.add(a.getDepartmentID());
            String depName= a.getName();
            cbKhoa.addItem(depName);
        }
    String departmentName= couDTO.getDepartment().getName();
    System.out.println(departmentName);
    cbKhoa.setSelectedItem(departmentName);    
}
